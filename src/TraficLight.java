public class TraficLight {
    public static void main(String[] args) {
        int i = 1;

        if (i == 0) {
            System.out.println("Stop");
        } else if (i == 2) {
            System.out.println("Wait");
        } else if (i == 1) {
            System.out.println("Go");
        } else {
            System.out.println("Error");
        }
    }
}
